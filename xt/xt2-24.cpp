#include <iostream>
using namespace std;
int main()
{
    int a = 1, b = 2, c = 3;
    cout << (a | b - c) << endl;
    //原式：a|(b-c)=(-c+b)|a
    //     -c=0b10000011
    // -c补码:0b11111101
    //  b补码:0b00000010
    //    b-c=0b11111111
    //b-c补码:0b10000001
    //      a=0b00000001
    //  a|b-c=0b10000001=-1
    cout << (a ^ b & -c) << endl;
    //0b00000010&
    //0b10000011补码
    //0b11111101
    //0b00000000=0^
    //0b00000001
    //0b00000001=1
    cout << (a & b | c) << endl;
    //0b00000001&
    //0b00000010=
    //0b00000000=0|
    //0b00000011
    //0b00000011=3
    cout << (a | b & c) << endl;
    //0b00000010&
    //0b00000011=
    //0b00000010=2|
    //0b00000001=
    //0b00000011=3
    cout << (!a | a) << endl;//1
    //a=1,!a=0;
    //0b00000000
    //0b00000001
    //0b00000001
    cout << (~a | a) << endl;//-1
    // a=0b00000001
    //~a=0b11111110
    //0b11111111
    //??0b10000001=-1
    cout << (a ^ a) << endl;//0
    //0b00000001
    //0b00000000=0
    cout << (a >> 2) << endl;//0
    //0b00000001>>2
    //0b00000000=0
    getchar();
    return 0;
}
